from groq import Groq
import sys

# Гарантируем UTF‑8 вывод
sys.stdout.reconfigure(encoding='utf-8')

client = Groq()


def get_description(title: str, authors: str = "") -> str:
    """
    Генерирует краткое структурированное описание книги через Groq Llama 3.3 70B.
    Возвращает строку или 'Описание недоступно'.
    """

    prompt = f"""
Проанализируй описание книги и создай структурированное, нейтральное, краткое описание для эмбеддингов. 
Строго соблюдай формат и правила ниже.

ФОРМАТ ВЫВОДА (строго):
жанр: <1 жанр из списка>
темы: <3–6 из списка>
конфликт: <1–2 короткие нейтральные фразы>
смысл: <1–2 нейтральных предложения>

ТРЕБОВАНИЯ:
1. НЕ добавляй ничего лишнего.
2. НЕ используй художественные описания, эмоции, оценки, интерпретации.
3. НЕ придумывай жанры — выбирай строго из списка ниже.
4. Темы должны быть короткими (1–2 слова), нейтральными, без оценок.
5. Конфликт — только основное противоречие, без деталей сюжета.
6. Смысл — краткое обобщение, без анализа стиля и без морализаторства.
7. Пиши строго в указанном формате, без дополнительных комментариев.

СПИСОК ЖАНРОВ (выбери один):
роман, повесть, рассказ, новелла, притча, эссе, пьеса, драма, трагедия, комедия, классическая проза, классическая поэзия, научная фантастика, социальная фантастика, философская фантастика, фэнтези, магический реализм, антиутопия, утопия, приключения, исторические приключения, путешествия, детектив, классический детектив, психологический детектив, криминальный роман, триллер, мистический триллер, сатира, сатирическая повесть, юмористическая проза, исторический роман, историческая проза, психологическая проза, психологический роман, научно‑популярная литература, популярная наука, биология (научпоп), физика и космос (научпоп), эволюция и антропология (научпоп), психология (научпоп), философия науки, история науки, философия, философская проза, религиозная литература, культурология, социология, политическая теория, биография, автобиография, мемуары, документальная проза, публицистика, репортаж, аллегория, басня, эпистолярная литература, дневниковая проза, очерк

СПИСОК ТЕМ (выбирай 3–6 или формулируй близкие по стилю):
любовь, дружба, семья, детство, взросление, одиночество, смерть, страдание, вина, прощение, смысл жизни, свобода, выбор, мораль, добро и зло, внутренний конфликт, самопознание, идентичность, психология личности, эмоции, власть, коррупция, бюрократия, социальное неравенство, революция, государство, политика, общество, социальные нормы, культура, традиции, война, насилие, конфликт, выживание, опасность, героизм, предательство, честь, долг, наука, технологии, искусственный интеллект, космос, эволюция, биология, физика, экология, будущее, прогресс, религия, духовность, мифология, верования, ритуалы, история, прошлое, цивилизация, социальные процессы, преступление, расследование, тайна, загадка, манипуляция, психопатология, отношения, конфликт поколений, социальные роли, гендер, класс, экономика, путешествия, путь героя, приключение, открытия, исследование, магия, мистика, сверхъестественное, фантастические элементы, творчество, искусство, красота, эстетика, работа, карьера, амбиции, успех, провал

НАЗВАНИЕ КНИГИ: {title}
АВТОР(Ы): {authors}
"""

    try:
        completion = client.chat.completions.create(
            model="llama-3.1-8b-instant",
            # llama-3.3-70b-versatile
            # meta-llama/llama-4-scout-17b-16e-instruct
            # llama-3.1-8b-instant
            messages=[{"role": "user", "content": prompt}],
            temperature=0.1,
            max_completion_tokens=300,
            top_p=1,
            stream=False
        )

        text = completion.choices[0].message.content.strip()

        # нормализация
        if not text or len(text) < 10:
            return "Описание недоступно"

        if "описание недоступно" in text.lower():
            return "Описание недоступно"

        return text

    except Exception as e:
        # безопасный вывод ошибки
        print("LLM ERROR:", str(e).encode("utf-8", "ignore").decode("utf-8"))
        return "Описание недоступно"
