def is_selfhelp(text: str) -> bool:
    if not isinstance(text, str):
        return False

    t = text.lower()

    for kw in SELFHELP_KEYWORDS:
        if kw in t:
            return True

    return False


import pandas as pd

df = pd.read_csv("books_filtered.csv")

# Маска: книга относится к саморазвитию / бизнесу / эзотерике / поп‑психологии
mask_remove = df.apply(
    lambda row: (
        is_selfhelp(str(row.get("title", ""))) or
        is_selfhelp(str(row.get("authors", ""))) or
        is_selfhelp(str(row.get("genres", "")))
    ),
    axis=1
)

df_removed = df[mask_remove]
df_filtered = df[~mask_remove]

df_filtered.to_csv("books_filtered.csv", index=False)
df_removed.to_csv("books_removed.csv", index=False)

print("Всего книг:", len(df))
